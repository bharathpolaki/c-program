#include <stdio.h>
int main()
{
    int a=546;
    int b=573;
    //greater than operator
    if(a>b);
    printf("%d is greater than %d\n");
    //less than operator
    if(a<b);
    printf("%d is less than %d\n");
    //greater than or equal to operator
    if(a>=b);
    printf("%d is greater than or equal to %d\n");
    //less than or equal to operator
    if(a<=b);
    printf("%d is less than or equal to %d\n");
    //equal to operater
    if(a==b);
    printf("%d is equal to %d\n");
    //not equal to operator
    if(a!=b);
    printf("%d is not equal to %d\n");
    return 0;
}
