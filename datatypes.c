#include <stdio.h>
int main()
{
    //integer value for short int in a
    short int a=111;
    //integer value for unsigned int b
    int b=104u;
    //integer value for positive int c
    int c=26;
    //integer value for negative value d
    int d=-53;
    //integer value for float e
    float e=2.5;
    printf("integer value for short int=%d\n",a);
    printf("integer value for unsigned int=%u\n",b);
    printf("integer value for positive int=%d\n",c);
    printf("integer value for negative int=%d\n",d);
    printf("integer value for float=%f\n",e);
    return 0;
}